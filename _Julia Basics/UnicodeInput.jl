# Unicode symbols can be used as variable names
a = 1
b = 2
c = -3
Δ = b ^ 2 - 4 * a * c
println(Δ)      # 16

# There are thousand of available characters for naming
☾ = "Anything"
★ = "Anything"
☼ = "Anything"
♈ = "Anything"
∱ = "Anything"

# And there are special characters for mathematical constants
println(1 * π)  # 3.141592653589793
println(1 * ℯ)  # 2.718281828459045

# And for mathematical operators
println(2 ≥ 1)  # true
println(0 ≠ 0)  # false
