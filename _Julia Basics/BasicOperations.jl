# Primitive mathematical operators (+ - *)
println(2 + 5)        # 7
println(2 + 5.0)      # 7.0
println(2 - 5)        # -3
println(2 - 5.0)      # -3.0
println(2 * 5)        # 10
println(2 * 5.0)      # 10.0

# Float division
println(84 / 2)       # 42.0 => (it makes the casting, even if the result is an integer)

# Integer division
println(div(84,2))    # 42

# Exponentiation
println(2^31)         # 2147483648
